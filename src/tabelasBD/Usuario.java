package tabelasBD;

public class Usuario {
		private int idUsuario;
		private String nomeUsuario;
		private int cpfUsuario;
		private String ufUsuario;
		private String emailUsuario;
		private int celularUsuario;
		private String generoUsuario;
		private String nascimentoUsuario; 
		private String senhaUsuario;
		
		public int getIdUsuario() {
			return idUsuario;
		}
		public void setIdUsuario(int idUsuario) {
			this.idUsuario = idUsuario;
		}
		public String getNomeUsuario() {
			return nomeUsuario;
		}
		public void setNomeUsuario(String nomeUsuario) {
			this.nomeUsuario = nomeUsuario;
		}
		public int getCpfUsuario() {
			return cpfUsuario;
		}
		public void setCpfUsuario(int cpfUsuario) {
			this.cpfUsuario = cpfUsuario;
		}
		public String getUfUsuario() {
			return ufUsuario;
		}
		public void setUfUsuario(String ufUsuario) {
			this.ufUsuario = ufUsuario;
		}
		public String getEmailUsuario() {
			return emailUsuario;
		}
		public void setEmailUsuario(String emailUsuario) {
			this.emailUsuario = emailUsuario;
		}
		public int getCelularUsuario() {
			return celularUsuario;
		}
		public void setCelularUsuario(int celularUsuario) {
			this.celularUsuario = celularUsuario;
		}
		public String getGeneroUsuario() {
			return generoUsuario;
		}
		public void setGeneroUsuario(String generoUsuario) {
			this.generoUsuario = generoUsuario;
		}
		public String getNascimentoUsuario() {
			return nascimentoUsuario;
		}
		public void setNascimentoUsuario(String nascimentoUsuario) {
			this.nascimentoUsuario = nascimentoUsuario;
		}
		public String getSenhaUsuario() {
			return senhaUsuario;
		}
		public void setSenhaUsuario(String senhaUsuario) {
			this.senhaUsuario = senhaUsuario;
		}

		
	}


