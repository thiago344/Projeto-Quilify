package tabelasBD;


	public class TabelaProf {
		private int idProf;
		private String nomeProf;
		private int cpfProf;
		private String ufProf;
		private String emailProf;
		private int celularProf;
		private String generoProf;
		private String nascimentoProf; 
		private String senhaProf;
		public int getIdProf() {
			return idProf;
		}
		public void setIdProf(int idProf) {
			this.idProf = idProf;
		}
		public String getNomeProf() {
			return nomeProf;
		}
		public void setNomeProf(String nomeProf) {
			this.nomeProf = nomeProf;
		}
		public int getCpfProf() {
			return cpfProf;
		}
		public void setCpfProf(int cpfProf) {
			this.cpfProf = cpfProf;
		}
		public String getUfProf() {
			return ufProf;
		}
		public void setUfProf(String ufProf) {
			this.ufProf = ufProf;
		}
		public String getEmailProf() {
			return emailProf;
		}
		public void setEmailProf(String emailProf) {
			this.emailProf = emailProf;
		}
		public int getCelularProf() {
			return celularProf;
		}
		public void setCelularProf(int celularProf) {
			this.celularProf = celularProf;
		}
		public String getGeneroProf() {
			return generoProf;
		}
		public void setGeneroProf(String generoProf) {
			this.generoProf = generoProf;
		}
		public String getNascimentoProf() {
			return nascimentoProf;
		}
		public void setNascimentoProf(String nascimentoProf) {
			this.nascimentoProf = nascimentoProf;
		}
		public String getSenhaProf() {
			return senhaProf;
		}
		public void setSenhaProf(String senhaProf) {
			this.senhaProf = senhaProf;
		}

	}



